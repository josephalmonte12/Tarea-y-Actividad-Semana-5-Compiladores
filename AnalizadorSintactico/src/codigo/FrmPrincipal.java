    private void btnAnalizarSinActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAnalizarSinActionPerformed
        // TODO add your handling code here:
        String ST = txtResultado.getText();
        Sintax s = new Sintax(new codigo.LexerCup(new StringReader(ST)));
        
        try {
            s.parse();
            txtAnalizarSin.setText("Analisis realizado correctamente");
            txtAnalizarSin.setForeground(new Color(25, 111, 61));
        } catch (Exception ex) {
            Symbol sym = s.getS();
            txtAnalizarSin.setText("Error de sintaxis. Linea: " + (sym.right + 1) + " Columna: " + (sym.left + 1) + ", Texto: \"" + sym.value + "\"");
            txtAnalizarSin.setForeground(Color.red);
            Seman((sym.right + 1),(sym.left + 1),sym.value);
        }
    }//GEN-LAST:event_btnAnalizarSinActionPerformed

    
private void Seman(Object rigth, Object left, Object valor) {
    if (rigth.equals("1")) { // Verificar si rigth es "1"
        txtAnalizarSin.setText("Error de sintaxis. Linea: " + rigth + " Columna: " + left + ", Texto: \"" + valor + "\""+
                "\nPosible solucion Semántica: Inicializar la función main");
        txtAnalizarSin.setForeground(Color.red);
        return; // Salir del método
    }

    if (valor != null) { // Verificar si valor no es nulo
        String val = valor.toString();
        int val2 = 2;
        
        switch (val) {
            case "=":
                txtAnalizarSin.setText("Error de sintaxis. Linea: " + rigth + " Columna: " + left + ", Texto: \"" + valor + "\""+
                        "\nPosible solución Semántica: Declarar un identificador");
                txtAnalizarSin.setForeground(Color.red);
                break;
            case "}":
                txtAnalizarSin.setText("Error de sintaxis. Linea: " + rigth + " Columna: " + left + ", Texto: \"" + valor + "\""+
                        "\nPosible solución Semántica: Variables sin cerrar, no se encontró un retorno o acción");
                txtAnalizarSin.setForeground(Color.red);
                break;
            case "int":
                txtAnalizarSin.setText("Error de sintaxis. Linea: " + rigth + " Columna: " + left + ", Texto: \"" + valor + "\""+
                        "\nPosible solución Semántica: Falta cerrar la variable");
                txtAnalizarSin.setForeground(Color.red);
                break;
            case "\"":
                txtAnalizarSin.setText("Error de sintaxis. Linea: " + rigth + " Columna: " + left + ", Texto: \"" + valor + "\""+
                        "\nPosible solución Semántica: Los datos no son compatibles");
                txtAnalizarSin.setForeground(Color.red);
                break;
            case ")":
                txtAnalizarSin.setText("Error de sintaxis. Linea: " + rigth + " Columna: " + left + ", Texto: \"" + valor + "\""+
                        "\nPosible solución Semántica: Llenar los campos necesarios entre los paréntesis");
                txtAnalizarSin.setForeground(Color.red);
                break;
            case "(":
                txtAnalizarSin.setText("Error de sintaxis. Linea: " + rigth + " Columna: " + left + ", Texto: \"" + valor + "\""+
                        "\nPosible solución Semántica: Llenar los campos necesarios entre los paréntesis, no necesario en este campo");
                txtAnalizarSin.setForeground(Color.red);
                break;
            case "+":
            case "-":
            case "/":
            case "*":
                txtAnalizarSin.setText("Error de sintaxis. Linea: " + rigth + " Columna: " + left + ", Texto: \"" + valor + "\""+
                        "\nPosible solución Semántica: Operador colocado indebidamente");
                txtAnalizarSin.setForeground(Color.red);
                break;
        }

        switch (val2) {
            case 3:
                txtAnalizarSin.setText("Error de sintaxis. Linea: " + rigth + " Columna: " + left + ", Texto: \"" + valor + "\""+
                        "\nPosible solución Semántica: Los datos no son compatibles");
                txtAnalizarSin.setForeground(Color.red);
                break;
        }
    } else {
        // Tratar el caso cuando valor es nulo
        txtAnalizarSin.setText("Error de sintaxis. Linea: " + rigth + " Columna: " + left + ", Texto: \"" + valor + "\""+
                "\nPosible solución Semántica: La variable valor es nula");
        txtAnalizarSin.setForeground(Color.red);
    }
} 
